#include "main.h"

/**
 * add - sums
 * @a: num1
 * @b: num2
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts
 * @a: num1
 * @b: num2
 * Return: dif
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies
 * @a: num1
 * @b: num2
 * Return: mul
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - division
 * @a: num1
 * @b: num2
 * Return: div
 */
int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - moduls
 * @a: num1
 * @b: num2
 * Return: mod
 */
int mod(int a, int b)
{
        return (a % b);
}
